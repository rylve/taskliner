/** @type {Locale} */
let currentLocale = "en";

/** @returns {Locale} */
export function detectBrowserLocale() {
  const candidates = [
    ...(Array.isArray(navigator.languages) ? navigator.languages : []),
    navigator.language || "",
  ];
  for (const raw of candidates) {
    const lang = String(raw || "").toLowerCase();
    if (lang.startsWith("ja")) return "ja";
  }
  return "en";
}

/** @returns {Locale} */
export function loadStoredLocale() {
  try {
    const saved = localStorage.getItem(LOCALE_STORAGE_KEY);
    if (saved === "ja" || saved === "en") return saved;
  } catch {
    /* ignore */
  }
  // Respect the browser's preferred language until the user makes an explicit choice.
  return detectBrowserLocale();
}

/** @returns {Locale} */
export function getLocale() {
  return currentLocale;
}

/** BCP 47 tag for Intl / localeCompare */
export function getLocaleTag() {
  return currentLocale === "ja" ? "ja" : "en";
}

/**
 * @param {string} key
 * @param {Record<string, string | number>} [vars]
 */
export function t(key, vars) {
  const table = MESSAGES[currentLocale] || MESSAGES.en;
  let text = table[key] ?? MESSAGES.en[key] ?? key;
  if (vars) {
    for (const [name, value] of Object.entries(vars)) {
      text = text.replaceAll(`{${name}}`, String(value));
    }
  }
  return text;
}

function setAttrFromKey(el, attr, key) {
  if (!key) return;
  el.setAttribute(attr, t(key));
}

/** Apply data-i18n / data-i18n-* attributes in the document. */
export function applyDomI18n(root = document) {
  document.documentElement.lang = currentLocale;

  for (const el of root.querySelectorAll("[data-i18n]")) {
    el.textContent = t(el.getAttribute("data-i18n") || "");
  }
  for (const el of root.querySelectorAll("[data-i18n-html]")) {
    el.innerHTML = t(el.getAttribute("data-i18n-html") || "");
  }
  for (const el of root.querySelectorAll("[data-i18n-placeholder]")) {
    el.setAttribute("placeholder", t(el.getAttribute("data-i18n-placeholder") || ""));
  }
  for (const el of root.querySelectorAll("[data-i18n-title]")) {
    setAttrFromKey(el, "title", el.getAttribute("data-i18n-title"));
  }
  for (const el of root.querySelectorAll("[data-i18n-aria-label]")) {
    setAttrFromKey(el, "aria-label", el.getAttribute("data-i18n-aria-label"));
  }
  for (const el of root.querySelectorAll("[data-i18n-aria-label-dyn]")) {
    // kept for elements whose aria-label is set dynamically elsewhere
  }

  syncLanguageControls(root);
}

function syncLanguageControls(root = document) {
  const code = currentLocale === "ja" ? "JA" : "EN";
  for (const el of root.querySelectorAll("[data-lang-code]")) {
    el.textContent = code;
  }
  for (const btn of root.querySelectorAll("[data-set-locale]")) {
    const loc = btn.getAttribute("data-set-locale");
    const active = loc === currentLocale;
    btn.classList.toggle("is-active", active);
    btn.setAttribute("aria-pressed", active ? "true" : "false");
    btn.setAttribute("aria-selected", active ? "true" : "false");
  }
}

/**
 * @param {Locale} locale
 * @param {{ persist?: boolean }} [opts]
 */
export function setLocale(locale, opts = {}) {
  if (locale !== "ja" && locale !== "en") return currentLocale;
  const persist = opts.persist !== false;
  currentLocale = locale;
  if (persist) {
    try {
      localStorage.setItem(LOCALE_STORAGE_KEY, locale);
    } catch {
      /* ignore */
    }
  }
  applyDomI18n();
  window.dispatchEvent(new CustomEvent("taskliner:localechange", { detail: { locale } }));
  return currentLocale;
}

/** Initialize locale from storage / browser and apply DOM strings. */
export function initI18n() {
  currentLocale = loadStoredLocale();
  // Auto-detection is only a default; explicit choices are persisted by setLocale().
  applyDomI18n();
  return currentLocale;
}
